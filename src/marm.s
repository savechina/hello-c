.globl _start

// 假设代码从某个起始地址加载并执行
// 在ARM64系统中，这通常由固件决定，而不是固定为0x7C00
// 为了模拟，我们这里只是作为一个起始点
_start:
    // 如果系统支持，可以通过 WFI (Wait For Interrupt) 指令进入低功耗状态
    // 类似于 x86 的 HLT，等待中断发生。
    // 这比无限循环更节能。
    wfi

    // 如果 WFI 之后由于某种原因执行流继续（例如接收到中断，然后中断服务例程返回）
    // 我们需要一个无限循环来防止程序意外执行到未定义区域。
halt_loop:
    b halt_loop  // 无限循环，等同于 x86 的 jmp .halt

    // 在ARM64汇编中，我们通常不会像x86引导扇区那样填充特定大小并添加魔术字。
    // 这些是x86 BIOS引导过程的特定要求。
    // 在ARM64中，可执行文件或固件镜像的格式由工具链和系统启动规范定义。
